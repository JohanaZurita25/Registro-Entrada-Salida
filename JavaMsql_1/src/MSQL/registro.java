    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          Statement comando;
            if (isEditMode == false){
                try {
                    comando = conn.createStatement(); 
                    String sql = "INSERT INTO registro_usuarios (nombre, apellido_paterno, apellido_materno, fotografia, cargo, rfid) VALUES ('" + Nombre.getText() +"', '" + apellidoPaterno.getText() + "', ' " +  apellidoMaterno.getText() +" ', '' , '" + cargo.getText() + "', '" + rfid.getText() + "')";
                    comando.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Se ha registrado a " + Nombre.getText() + " exitosamente en la base de datos", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);

                    Nombre.setText("");
                    apellidoPaterno.setText("");
                    apellidoMaterno.setText("");
                    cargo.setText("");
                    rfid.setText("");
           
                } catch (SQLException ex){
                    JOptionPane.showMessageDialog(null, "Ha ocurrido un error al intentar registrar a " + Nombre.getText() + " en la base de datos", "Registro exitoso", JOptionPane.ERROR_MESSAGE);

                    Logger.getLogger(MSQL.class.getName()).log(Level.SEVERE, null, ex);

            }
        } else {
            try {
                comando = conn.createStatement(); 
                String sql = "UPDATE registro_usuarios SET nombre = '" + Nombre.getText() +  "' , apellido_paterno = '" + apellidoPaterno.getText() +"', apellido_materno = '" + apellidoMaterno.getText() + "', cargo = '" + cargo.getText() + "' , rfid = '"+ rfid.getText() +"' WHERE id = " + id;
                comando.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Se ha gaurdado a " + Nombre.getText() + " exitosamente en la base de datos", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } catch (SQLException ex){
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error al intentar registrar a " + Nombre.getText() + " en la base de datos", "Registro exitoso", JOptionPane.ERROR_MESSAGE);

                Logger.getLogger(MSQL.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

