    public void cargarListaUsuarios()
    {
        
        DefaultTableModel model = new DefaultTableModel();
        
        String consulta = "SELECT * FROM registro_usuarios";
        
        Statement resultado;
        try {
            resultado = conn.createStatement();
            ResultSet rs = resultado.executeQuery(consulta); 
            tablaUsuarios.setModel(buildTableModel(rs));
            System.out.println("Lista de usuarios cargada!");
            rs.close();
              
           
        } catch (SQLException ex) {
            Logger.getLogger(MSQL.class.getName()).log(Level.SEVERE, null, ex);
        }

        
    }

    public static DefaultTableModel buildTableModel(ResultSet rs)
        throws SQLException {

    ResultSetMetaData metaData = rs.getMetaData();

    // names of columns
    Vector<String> columnNames = new Vector<String>();
    int columnCount = metaData.getColumnCount();
    for (int column = 1; column <= columnCount; column++) {
        columnNames.add(metaData.getColumnName(column));
    }

    // data of the table
    Vector<Vector<Object>> data = new Vector<Vector<Object>>();
    while (rs.next()) {
        Vector<Object> vector = new Vector<Object>();
        for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
            vector.add(rs.getObject(columnIndex));
        }
        data.add(vector);
    }

    return new DefaultTableModel(data, columnNames);

}
